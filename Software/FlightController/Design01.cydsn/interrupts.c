CY_ISR(isr_usr_btn_handling){
    
    if(USR_LED_Read()){
        USR_LED_Write(0);
    }
    else{
        USR_LED_Write(1);
    }
    CyDelay(1000);
    USR_BTN_ClearInterrupt();
    
}
